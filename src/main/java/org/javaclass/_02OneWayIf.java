package org.javaclass;

public class _02OneWayIf {

    public static void main(String[] args) {

//-                                 THE ONE-WAY-IF SELECTION STATEMENT


//`     KEYWORD     SENTINEL VALUE       BODY
        if          (35 > 32)           {


        }


//`     The  'if' keyword denotes the start of the statement

//`     The 'sentinel value' is a boolean expression used to permit access (true) or
//`     restrict access (false) to the code body

//`     The code body is the braces and all code to be executed given a true sentinel value






//-                                         *CHECK POINT*


//     Evaluate 'sentinel value' true or false?
        if (55 > 52) {
            System.out.println();
        }


//`    Evaluate 'sentinel value' true or false?
        if (32 > 52) {
            System.out.println();
        }


//     Evaluate 'sentinel value' true or false?
        if (10 < 22) {
            System.out.println();
        }


//`    Evaluate 'sentinel value' true or false?
        if (52 < 52) {
            System.out.println();
        }


//     Evaluate 'sentinel value' true or false?
        if (52 <= 52) {
            System.out.println();
        }
    }
}