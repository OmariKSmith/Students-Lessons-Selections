//`     KEYWORD     SENTINEL VALUE       BODY
        if          (65 < 37)           {

        }
        else {

        }


//      The 'if' code body is the braces and all code to be executed given a true sentinel value

//`     The 'else' keyword marks the start of the alternative code body.
//`     This is the code that is executed given a false sentinel value






//-                                    CHECK POINT


//     Evaluate 'sentinel value'    then choose flow
        if (55 > 52) {
            //FLOW 1
        } else {
            //`FLOW 2
        }



//`    Evaluate 'sentinel value'    then choose flow
        if (32 < 52) {
            //`FLOW 1
        } else {
            //FLOW 2
        }



